    <h1>Utilisation des données personnelles collectées</h1>
    <p>Le Greta du Var collecte les noms et prénoms des stagiaires/étudiants, leur adresse, leur e-mail, leur numéro de
        téléphone, leur date de naissance, leur parcours de formation et professionnel, leur photographie. Les
        informations dites lors des entretiens peuvent aussi être enregistrées lorsque celles-ci sont utiles à la
        formation. Cependant aucunes données sensibles ne seront enregistrées sans l’accord écrit du stagiaire/étudiant.
        Après consentement du stagiaire/étudiant, ces informations sont récoltées et stockées un an à compter de
        l’inscription du stagiaire/étudiant afin de pouvoir inscrire le stagiaire/étudiant à la formation souhaitée, le
        contacter quand cela est nécessaire pour la formation et l’aider au mieux tout au long de la formation.
        Le traitement de vos données personnelles peut également être imposé par des dispositions légales ou
        réglementaires, telles que :
    <ul>
        <li>L’obligation de transmettre des données vous concernant aux organismes de protection sociale afin qu’ils
            déterminent vos droits sociaux</li>

        <li>
            L’obligation de transmettre tout document afférent aux prestations dont vous avez bénéficié dans le cadre du
            contrôle des financements mobilisés.
        </li>
    </ul>

    Votre adresse IP, l’agent utilisateur de votre navigateur ainsi que l’heure de votre connexion sont récoltées
    lors d’envoi des formulaires de contact, de commentaires et témoignages afin de nous aider à détecter ceux qui
    sont indésirables.
    Les informations concernant les financements des formations (carte bancaire, compte CPF) ne sont pas
    enregistrées. Elles ne sont en aucun cas transmises à un tiers.
    </p>
