        <h2>Cookies</h2>
        <p>
            Un cookie est un fichier texte déposé lors de la consultation d’un site, d’une application mobile ou d’une
            publicité en ligne et stocké dans un espace spécifique du disque dur de votre ordinateur ou de votre
            appareil mobile. Les cookies sont gérés par votre navigateur Internet ou l’application mobile que vous
            utilisez, et seul l’émetteur d’un cookie peut décider de la lecture ou de la modification des informations
            qui y sont contenues.
            <br><br>
            Si vous vous rendez sur la page de connexion, un cookie temporaire sera créé afin de déterminer si votre
            navigateur accepte les cookies. Il ne contient pas de données personnelles et sera supprimé automatiquement
            à la fermeture de votre navigateur.
            <br><br>
            Lors de votre connexion à votre espace, des cookies pour enregistrer vos informations de connexion et vos
            préférences d’écran sont mis en place. La durée de vie d’un cookie de connexion est de deux jours et celle
            d’un cookie d’option d’écran est d’un an. Si vous cochez l’option « se souvenir de moi », votre cookie de
            connexion sera conservé deux semaines. Ce dernier sera effacé si vous vous déconnectez de votre compte.
        </p>
    </div>

